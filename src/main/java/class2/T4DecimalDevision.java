package class2;

public class T4DecimalDevision {
    public static void main(String[] args) {

        double num1=15;
        double num2=2;
        double addition=num1+num2;
        double subtract=num1-num2;
        double multiplication=num1*num2;
        double division=num1/num2;
        System.out.println("The adddition of "+num1+" and " +num2+ " is equals to "+addition);
        System.out.println("The subtraction of "+num1+" and "+num2+" is equal to "+subtract);
        System.out.println("The multiplication of "+num1+" and "+num2+" is equal to "+multiplication);
        System.out.println("The division of "+num1+" and "+num2+" is equal to "+division);



    }
}
